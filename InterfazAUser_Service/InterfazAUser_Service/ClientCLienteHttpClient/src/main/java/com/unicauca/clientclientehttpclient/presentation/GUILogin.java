    public Long verificarInicioSesion(String email, String password){
        String seleccionado = (String) cbxRoles.getSelectedItem();
        System.out.println("Rol escogido: "+seleccionado);
        List<User> users = service.findByRoleName(seleccionado);
        for(User user : users){
            System.out.println("User de rol: "+user.getUserName());
        }
        
        Long idActual;
        for (User user : users) {
            if (user.getUserEmail().equals(email) && user.getUserPassword().equals(password)) {
                idActual = user.getUserId();
                System.out.println("INICIO DE SESION CORRECTO");
                return idActual;
            }
        }
        return -1L;
    }
    
    
