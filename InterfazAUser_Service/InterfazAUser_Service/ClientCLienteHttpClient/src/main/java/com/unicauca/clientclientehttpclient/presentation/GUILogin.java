    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        
        if (txtEmail.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el email", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmail.requestFocus();
            return;
        }
        if (txtPassword.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la contraseña", "Error", JOptionPane.ERROR_MESSAGE);
            txtPassword.requestFocus();
            return;
        }
        
        String email = txtEmail.getText();
        String contrasenia = txtPassword.getText();
        
        Long idActual = verificarInicioSesion(email,contrasenia);
        if(idActual != -1){
            JOptionPane.showMessageDialog(null, "Inicio de sesión exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            GUIClient instance = new GUIClient(idActual);
            instance.setVisible(true);
           
        }else{
            JOptionPane.showMessageDialog(null, "Inicio de sesión fallido. Verifica tu email, contraseña y rol.", "Error",JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

