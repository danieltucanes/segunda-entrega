    public void llenarRoles(){
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        List<Role> roles = service.findAllRoles();
        for(Role role : roles){
            model.addElement(role.getRoleName());
        }
        cbxRoles.setModel(model);
    }
