    private List<Role> buscarRolVisitante(){
        List<Role> rolesRet = new ArrayList<>();
        List<Role> roles = service.findAllRoles();
        for(Role role : roles){
            if(role.getRoleName().equals("Visitante_Registrado")){
                System.out.println("Entra a visitante reg");
                rolesRet.add(role);
            }
        }
        return rolesRet;
    }

