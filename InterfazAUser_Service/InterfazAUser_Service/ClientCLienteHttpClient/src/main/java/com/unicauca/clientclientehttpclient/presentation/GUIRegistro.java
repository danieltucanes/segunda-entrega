    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
            
            Client client = new Client();
            User user = new User();
            if (txtfirstName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar su nombre", "Error",JOptionPane.ERROR_MESSAGE);         
                txtfirstName.requestFocus();
                return;
            }
            
            if (txtlastName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar su apellido", "Error",JOptionPane.ERROR_MESSAGE);         
                txtlastName.requestFocus();
                return;
            }
            
            if (txtaddress.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar una dirección", "Error",JOptionPane.ERROR_MESSAGE);         
                txtaddress.requestFocus();
                return;
            }
            
            if (txtUserName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar un nombre de usuario", "Error",JOptionPane.ERROR_MESSAGE);         
                txtUserName.requestFocus();
                return;
            }
            
            if (txtEmail.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar un correo electrónico", "Error",JOptionPane.ERROR_MESSAGE);         
                txtEmail.requestFocus();
                return;
            }
            
            if (txtPassword.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar una contraseña", "Error",JOptionPane.ERROR_MESSAGE);         
                txtPassword.requestFocus();
                return;
            }
            
            String nombre = txtfirstName.getText();
            String apellido = txtlastName.getText();
            String direccion = txtaddress.getText();
            String username = txtUserName.getText();
            String email = txtEmail.getText();
            String contrasenia = txtPassword.getText();
            client.setFirstName(nombre);
            client.setLastName(apellido);
            client.setAddress(direccion);
            user.setUserName(username);
            user.setUserEmail(email);
            user.setUserPassword(contrasenia);
            
            List<Role> role = buscarRolVisitante();
            
            user.setUserRoles(role);
            
            service.create(client, user);  
            
            RegresarPcpal();
    }//GEN-LAST:event_btnConfirmarActionPerformed

